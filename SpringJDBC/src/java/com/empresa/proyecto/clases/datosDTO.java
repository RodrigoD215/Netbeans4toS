package com.empresa.proyecto.clases;

import org.hibernate.validator.constraints.NotEmpty;

/**
 *
 * @author Rodrigo
 */
public class datosDTO {
    private int codigo;
    @NotEmpty(message = "Rellene el espacio en blanco")
    private String usuario,clave,nombres,direccion,telefono,correo,sexo;

    public datosDTO() {
    }

    public datosDTO(int codigo, String usuario, String clave, String nombres, String direccion, String telefono, String correo,String sexo) {
        this.codigo = codigo;
        this.usuario = usuario;
        this.clave = clave;
        this.nombres = nombres;
        this.direccion = direccion;
        this.telefono = telefono;
        this.correo = correo;
        this.sexo = sexo;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

   

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }


    
    
}
